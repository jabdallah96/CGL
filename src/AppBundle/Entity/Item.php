<?php

namespace AppBundle\Entity;

/**
 * Item
 */
class Item
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $quantity;

    /**
     * @var integer
     */
    private $code;

    /**
     * @var string
     */
    private $details;

    /**
     * @var integer
     */
    private $unit;

    /**
     * @var integer
     */
    private $total;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set quantity
     *
     * @param integer $quantity
     *
     * @return Item
     */
    public function setQuantity($quantity)
    {
        $this->quantity = $quantity;

        return $this;
    }

    /**
     * Get quantity
     *
     * @return integer
     */
    public function getQuantity()
    {
        return $this->quantity;
    }

    /**
     * Set code
     *
     * @param integer $code
     *
     * @return Item
     */
    public function setCode($code)
    {
        $this->code = $code;

        return $this;
    }

    /**
     * Get code
     *
     * @return integer
     */
    public function getCode()
    {
        return $this->code;
    }

    /**
     * Set details
     *
     * @param string $details
     *
     * @return Item
     */
    public function setDetails($details)
    {
        $this->details = $details;

        return $this;
    }

    /**
     * Get details
     *
     * @return string
     */
    public function getDetails()
    {
        return $this->details;
    }

    /**
     * Set unit
     *
     * @param integer $unit
     *
     * @return Item
     */
    public function setUnit($unit)
    {
        $this->unit = $unit;

        return $this;
    }

    /**
     * Get unit
     *
     * @return integer
     */
    public function getUnit()
    {
        return $this->unit;
    }

    /**
     * Set total
     *
     * @param integer $total
     *
     * @return Item
     */
    public function setTotal($total)
    {
        $this->total = $total;

        return $this;
    }

    /**
     * Get total
     *
     * @return integer
     */
    public function getTotal()
    {
        return $this->total;
    }

    /**
     * @var \AppBundle\Entity\RequestJob
     */
    private $request_job;


    /**
     * Set requestJob
     *
     * @param \AppBundle\Entity\RequestJob $requestJob
     *
     * @return Item
     */
    public function setRequestJob(\AppBundle\Entity\RequestJob $requestJob = null)
    {
        $this->request_job = $requestJob;

        return $this;
    }

    /**
     * Get requestJob
     *
     * @return \AppBundle\Entity\RequestJob
     */
    public function getRequestJob()
    {
        return $this->request_job;
    }
}
