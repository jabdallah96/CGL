<?php

namespace AppBundle\Entity;

/**
 * WorkshopData
 */
class WorkshopData
{
    /**
     * @var string
     */
    private $shopdrawing;

    /**
     * @var string
     */
    private $partsRequest;

    /**
     * @var \DateTime
     */
    private $addedAt;


    /**
     * Set shopdrawing
     *
     * @param string $shopdrawing
     *
     * @return WorkshopData
     */
    public function setShopdrawing($shopdrawing)
    {
        $this->shopdrawing = $shopdrawing;

        return $this;
    }

    /**
     * Get shopdrawing
     *
     * @return string
     */
    public function getShopdrawing()
    {
        return $this->shopdrawing;
    }

    /**
     * Set partsRequest
     *
     * @param string $partsRequest
     *
     * @return WorkshopData
     */
    public function setPartsRequest($partsRequest)
    {
        $this->partsRequest = $partsRequest;

        return $this;
    }

    /**
     * Get partsRequest
     *
     * @return string
     */
    public function getPartsRequest()
    {
        return $this->partsRequest;
    }

    /**
     * Set addedAt
     *
     * @param \DateTime $addedAt
     *
     * @return WorkshopData
     */
    public function setAddedAt($addedAt)
    {
        $this->addedAt = $addedAt;

        return $this;
    }

    /**
     * Get addedAt
     *
     * @return \DateTime
     */
    public function getAddedAt()
    {
        return $this->addedAt;
    }
    /**
     * @var integer
     */
    private $id;

    /**
     * @var \AppBundle\Entity\Job
     */
    private $job;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set job
     *
     * @param \AppBundle\Entity\Job $job
     *
     * @return WorkshopData
     */
    public function setJob(\AppBundle\Entity\Job $job = null)
    {
        $this->job = $job;

        return $this;
    }

    /**
     * Get job
     *
     * @return \AppBundle\Entity\Job
     */
    public function getJob()
    {
        return $this->job;
    }
}
